import { execFileSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const savisDirectory = resolve(".");
const savouretplusDirectory = resolve(process.argv[2] || "../savouretplus");

const output = execFileSync(
  "npx",
  ["--yes", "supabase", "status", "-o", "env"],
  { encoding: "utf8" },
);

const values = Object.fromEntries(
  output
    .split(/\r?\n/)
    .map((line) => line.match(/^([A-Z0-9_]+)=(.*)$/))
    .filter(Boolean)
    .map((match) => [
      match[1],
      match[2].replace(/^["']|["']$/g, ""),
    ]),
);

const publicKey = values.ANON_KEY || values.PUBLISHABLE_KEY;
const serverKey = values.SERVICE_ROLE_KEY || values.SECRET_KEY;

if (!values.API_URL || !publicKey || !serverKey) {
  throw new Error(
    "Supabase local ne retourne pas les URL et clés attendues.",
  );
}

const supabaseUrl = new URL(values.API_URL);
supabaseUrl.hostname = "host.docker.internal";

writeFileSync(
  resolve(savisDirectory, ".env.supabase.local"),
  [
    readFileSync(resolve(savisDirectory, ".env.local"), "utf8").trimEnd(),
    "",
    "# Generated by SAVIS make run-local",
    "SUPABASE_ENABLED=true",
    `SUPABASE_URL=${supabaseUrl.origin}`,
    `SUPABASE_SERVICE_ROLE_KEY=${serverKey}`,
    "",
  ].join("\n"),
);

console.log("Adaptateur Supabase de SAVIS configuré.");

if (existsSync(savouretplusDirectory)) {
  writeFileSync(
    resolve(savouretplusDirectory, ".env.local"),
    [
      "# Generated by SAVIS make run-local",
      `VITE_SUPABASE_URL=${values.API_URL}`,
      `VITE_SUPABASE_ANON_KEY=${publicKey}`,
      "",
    ].join("\n"),
  );

  console.log(
    `Savouretplus configuré dans ${savouretplusDirectory}/.env.local.`,
  );
} else {
  console.warn(
    `Savouretplus introuvable dans ${savouretplusDirectory}; configuration ignorée.`,
  );
}
